package Arjun;
	public class DataVariables {
	    public static void main(String[] args) {
	        // Integer type variable
	        int age = 25;

	        // Floating-point type variable
	        float height = 5.9f;

	        // Double-precision floating-point type variable
	        double weight = 70.5;

	        // Character type variable
	        char initial = 'A';

	        // Boolean type variable
	        boolean isJavaFun = true;

	        // String type variable (not a primitive type, but widely used)
	        String name = "John Doe";

	        // Byte type variable
	        byte smallNumber = 120;

	        // Short type variable
	        short shortNumber = 30000;

	        // Long type variable
	        long largeNumber = 10000000000L;

	        // Print the values of variables
	        System.out.println("Name: " + name);
	        System.out.println("Age: " + age);
	        System.out.println("Height: " + height);
	        System.out.println("Weight: " + weight);
	        System.out.println("Initial: " + initial);
	        System.out.println("Is Java Fun? " + isJavaFun);
	        System.out.println("Small Number (byte): " + smallNumber);
	        System.out.println("Short Number: " + shortNumber);
	        System.out.println("Large Number: " + largeNumber);
	    }
	}

/*
Explanation:
int: Represents an integer (whole number). Example: age = 25.
float: Represents a floating-point number (single precision). Requires an f suffix. Example: height = 5.9f.
double: Represents a double-precision floating-point number. Example: weight = 70.5.
char: Represents a single character. Example: initial = 'A'.
boolean: Represents a true or false value. Example: isJavaFun = true.
String: Represents a sequence of characters (not a primitive type but commonly used). Example: name = "John Doe".
byte: Represents an 8-bit signed integer. Example: smallNumber = 120.
short: Represents a 16-bit signed integer. Example: shortNumber = 30000.
long: Represents a 64-bit signed integer. Requires an L suffix. Example: largeNumber = 10000000000L.
*/
